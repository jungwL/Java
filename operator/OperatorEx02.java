package javapro.operator;

public class OperatorEx02 {

	public static void main(String[] args) {
		// 정확한 계산은 정수 연산으로
		int apple=1;
		int totalpiece=apple*10;
		int number=7;
		int rs=totalpiece-number;
		System.out.println("10조각에서 남은 조각 = "+rs);
		System.out.println("1조각에서 남은 양 = "+rs/10.0);
		System.out.println(20-13.3);

	}

}
